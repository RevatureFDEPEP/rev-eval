from typing import List, Optional
from src.schemas.question import QuestionCreate, QuestionUpdate
from src.repositories.question_repository import QuestionRepository
from src.models.question import Question, QuestionType, Option, OptionCreate
from datetime import datetime, timezone
from fastapi import HTTPException


class QuestionService:
    """
    Service layer for question management with business logic and validation.

    This service provides CRUD operations with type-specific validation
    to ensure data integrity.
    """

    @staticmethod
    def convert_options_to_stored_format(options: Optional[List[OptionCreate]]) -> Optional[List[Option]]:
        """
        Convert OptionCreate (without option_id) to Option (with auto-generated option_id).

        Args:
            options: List of OptionCreate objects (just text)

        Returns:
            List of Option objects with auto-generated 1-indexed option_ids
        """
        if options is None:
            return None

        return [
            Option(option_id=idx, text=opt.text)
            for idx, opt in enumerate(options, start=1)
        ]

    @staticmethod
    async def create_question(data: QuestionCreate) -> str:
        """
        Create a new question with validation.

        MongoDB auto-generates the _id field.
        option_ids are auto-generated based on position (1-indexed).

        Args:
            data: QuestionCreate schema with validated data

        Returns:
            str: The MongoDB _id of the created question

        Raises:
            HTTPException: If validation fails
        """
        try:
            # Convert question data
            question_data = data.model_dump()

            # Auto-generate option_ids for MCQ/MULTI questions
            if question_data.get('options'):
                options_create = [OptionCreate(**opt) for opt in question_data['options']]
                question_data['options'] = [
                    opt.model_dump() for opt in QuestionService.convert_options_to_stored_format(options_create)
                ]

            # Create Question document (id is auto-generated by MongoDB)
            question = Question(**question_data)
            return await QuestionRepository.create(question)
        except ValueError as e:
            raise HTTPException(status_code=400, detail=str(e))
        except Exception as e:
            raise HTTPException(status_code=500, detail=f"Error creating question: {str(e)}")

    @staticmethod
    async def get_all_questions() -> List[Question]:
        """
        Retrieve all questions from the database.

        Returns:
            List[Question]: List of all Question documents
        """
        return await QuestionRepository.get_all()

    @staticmethod
    async def get_question_by_id(qid: str) -> Optional[Question]:
        """
        Retrieve a specific question by its ID.

        Args:
            qid: The question ID

        Returns:
            Optional[Question]: The Question document if found, None otherwise
        """
        return await QuestionRepository.get_by_id(qid)

    @staticmethod
    async def update_question(qid: str, data: QuestionUpdate) -> bool:
        """
        Update an existing question with type-aware validation.

        This method fetches the existing question, validates that the update
        is compatible with the question's type, and applies the update.

        Args:
            qid: The question ID to update
            data: QuestionUpdate schema with update data

        Returns:
            bool: True if updated successfully

        Raises:
            HTTPException: If validation fails or question not found
        """
        # Fetch existing question (now a Beanie Document)
        existing_question = await QuestionRepository.get_by_id(qid)
        if not existing_question:
            return False

        # Extract question type from existing question
        question_type = existing_question.type

        # Get update data, excluding None values
        update_data = {k: v for k, v in data.model_dump().items() if v is not None and k != "updated_at"}

        # Auto-generate option_ids if options are being updated
        if 'options' in update_data and update_data['options'] is not None:
            options_create = [OptionCreate(**opt) for opt in update_data['options']]
            update_data['options'] = [
                opt.model_dump() for opt in QuestionService.convert_options_to_stored_format(options_create)
            ]

        # Convert existing question to dict for validation
        existing_dict = existing_question.model_dump()

        # Perform type-specific validation for updates
        try:
            QuestionService._validate_update_for_type(
                question_type=question_type,
                existing_question=existing_dict,
                update_data=update_data
            )
        except ValueError as e:
            raise HTTPException(status_code=400, detail=str(e))

        # Add updated timestamp
        update_data["updated_at"] = datetime.now(timezone.utc)

        # Perform the update
        return await QuestionRepository.update(qid, update_data)

    @staticmethod
    def _validate_update_for_type(
        question_type: str,
        existing_question: dict,
        update_data: dict
    ) -> None:
        """
        Validate that update data is compatible with the question type.

        Args:
            question_type: The type of the question (mcq, multi, true_false, text)
            existing_question: The existing question data
            update_data: The data being updated

        Raises:
            ValueError: If update is incompatible with question type
        """
        if question_type == QuestionType.MCQ.value:
            # MCQ validation
            if "options" in update_data and update_data["options"] is not None:
                if len(update_data["options"]) < 2:
                    raise ValueError("MCQ questions must have at least 2 options")

            if "correct_answers" in update_data and update_data["correct_answers"] is not None:
                if len(update_data["correct_answers"]) != 1:
                    raise ValueError("MCQ questions must have exactly one correct answer")

                if not isinstance(update_data["correct_answers"][0], int):
                    raise ValueError("MCQ correct_answers must be an integer (option_id)")

            # Validate correct_answer is a valid option_id
            options = update_data.get("options") or existing_question.get("options", [])
            correct_answers = update_data.get("correct_answers") or existing_question.get("correct_answers", [])

            if options and correct_answers:
                valid_option_ids = [opt["option_id"] if isinstance(opt, dict) else opt.option_id for opt in options]
                if correct_answers[0] not in valid_option_ids:
                    raise ValueError(f"correct_answers {correct_answers[0]} is not a valid option_id")

        elif question_type == QuestionType.MULTI.value:
            # MULTI validation
            if "options" in update_data and update_data["options"] is not None:
                if len(update_data["options"]) < 2:
                    raise ValueError("MULTI questions must have at least 2 options")

            if "correct_answers" in update_data and update_data["correct_answers"] is not None:
                if len(update_data["correct_answers"]) == 0:
                    raise ValueError("MULTI questions must have at least one correct answer")

                if not all(isinstance(ans, int) for ans in update_data["correct_answers"]):
                    raise ValueError("MULTI correct_answers must be a list of integers (option_ids)")

                # Check for duplicates
                if len(update_data["correct_answers"]) != len(set(update_data["correct_answers"])):
                    raise ValueError("MULTI correct_answers contains duplicates")

            # Validate all correct answers are valid option_ids
            options = update_data.get("options") or existing_question.get("options", [])
            correct_answers = update_data.get("correct_answers") or existing_question.get("correct_answers", [])

            if options and correct_answers:
                valid_option_ids = [opt["option_id"] if isinstance(opt, dict) else opt.option_id for opt in options]
                for ans in correct_answers:
                    if ans not in valid_option_ids:
                        raise ValueError(f"correct_answers contains invalid option_id {ans}")

                # Cannot have all options as correct
                if len(correct_answers) == len(options):
                    raise ValueError("MULTI questions cannot have all options as correct answers")

        elif question_type == QuestionType.TRUE_FALSE.value:
            # TRUE_FALSE validation
            if "options" in update_data and update_data["options"] is not None:
                raise ValueError("TRUE_FALSE questions should not have options")

            if "correct_answers" in update_data and update_data["correct_answers"] is not None:
                if len(update_data["correct_answers"]) != 1:
                    raise ValueError("TRUE_FALSE questions must have exactly one correct answer")

                if not isinstance(update_data["correct_answers"][0], bool):
                    raise ValueError("TRUE_FALSE correct_answers must be a boolean value")

        elif question_type == QuestionType.TEXT.value:
            # TEXT validation
            if "options" in update_data and update_data["options"] is not None:
                raise ValueError("TEXT questions should not have options")

            if "correct_answers" in update_data and update_data["correct_answers"] is not None:
                raise ValueError("TEXT questions should not have correct_answers (use sample_answer instead)")

            if "sample_answer" in update_data:
                sample_answer = update_data["sample_answer"]
                if sample_answer is not None:
                    if not sample_answer.strip():
                        raise ValueError("TEXT questions must have a non-empty sample_answer")
                    if len(sample_answer.strip()) < 10:
                        raise ValueError("TEXT sample_answer must be at least 10 characters long")

    @staticmethod
    async def delete_question(qid: str) -> bool:
        """
        Delete a question by its ID.

        Args:
            qid: The question ID to delete

        Returns:
            bool: True if deleted successfully, False if not found
        """
        return await QuestionRepository.delete(qid)

    @staticmethod
    async def find_by_type(question_type: str, limit: int = 100) -> List[Question]:
        """
        Find questions by type.

        Args:
            question_type: The type of questions (mcq, multi, true_false, text)
            limit: Maximum number of questions to return

        Returns:
            List[Question]: List of matching questions

        Raises:
            HTTPException: If question_type is invalid
        """
        # Validate question type
        valid_types = [qt.value for qt in QuestionType]
        if question_type not in valid_types:
            raise HTTPException(
                status_code=400,
                detail=f"Invalid question type. Must be one of: {', '.join(valid_types)}"
            )

        return await QuestionRepository.find_by_type(question_type, limit)

    @staticmethod
    async def find_by_skill(skill: str, limit: int = 100) -> List[Question]:
        """
        Find questions by skill.

        Args:
            skill: The skill to search for
            limit: Maximum number of questions to return

        Returns:
            List[Question]: List of matching questions
        """
        if not skill or not skill.strip():
            raise HTTPException(status_code=400, detail="Skill parameter cannot be empty")

        return await QuestionRepository.find_by_skill(skill.strip(), limit)

    @staticmethod
    async def find_by_difficulty(difficulty: str, limit: int = 100) -> List[Question]:
        """
        Find questions by difficulty level.

        Args:
            difficulty: The difficulty level (easy, medium, hard)
            limit: Maximum number of questions to return

        Returns:
            List[Question]: List of matching questions

        Raises:
            HTTPException: If difficulty is invalid
        """
        valid_difficulties = ["easy", "medium", "hard"]
        if difficulty not in valid_difficulties:
            raise HTTPException(
                status_code=400,
                detail=f"Invalid difficulty. Must be one of: {', '.join(valid_difficulties)}"
            )

        return await QuestionRepository.find_by_difficulty(difficulty, limit)

    @staticmethod
    async def find_by_tags(tags: List[str], limit: int = 100) -> List[Question]:
        """
        Find questions that have any of the specified tags.

        Args:
            tags: List of tags to search for
            limit: Maximum number of questions to return

        Returns:
            List[Question]: List of matching questions
        """
        if not tags or len(tags) == 0:
            raise HTTPException(status_code=400, detail="At least one tag must be provided")

        # Strip whitespace from tags
        cleaned_tags = [tag.strip() for tag in tags if tag.strip()]
        if len(cleaned_tags) == 0:
            raise HTTPException(status_code=400, detail="At least one valid tag must be provided")

        return await QuestionRepository.find_by_tags(cleaned_tags, limit)

    @staticmethod
    async def filter_questions(
        question_type: Optional[str] = None,
        skill: Optional[str] = None,
        difficulty: Optional[str] = None,
        tags: Optional[List[str]] = None,
        limit: int = 100
    ) -> List[Question]:
        """
        Filter questions by multiple criteria.

        Args:
            question_type: Optional question type filter
            skill: Optional skill filter
            difficulty: Optional difficulty filter
            tags: Optional list of tags
            limit: Maximum number of questions to return

        Returns:
            List[Question]: List of matching questions

        Raises:
            HTTPException: If no filters provided or invalid parameters
        """
        # Ensure at least one filter is provided
        if not any([question_type, skill, difficulty, tags]):
            raise HTTPException(
                status_code=400,
                detail="At least one filter parameter must be provided"
            )

        # Build query conditions for Beanie
        from beanie.operators import And, In as BeanieIn

        conditions = []

        # Add type filter
        if question_type:
            valid_types = [qt.value for qt in QuestionType]
            if question_type not in valid_types:
                raise HTTPException(
                    status_code=400,
                    detail=f"Invalid question type. Must be one of: {', '.join(valid_types)}"
                )
            conditions.append(Question.type == question_type)

        # Add skill filter
        if skill and skill.strip():
            conditions.append(BeanieIn(Question.skills, [skill.strip()]))

        # Add difficulty filter
        if difficulty:
            valid_difficulties = ["easy", "medium", "hard"]
            if difficulty not in valid_difficulties:
                raise HTTPException(
                    status_code=400,
                    detail=f"Invalid difficulty. Must be one of: {', '.join(valid_difficulties)}"
                )
            conditions.append(Question.difficulty == difficulty)

        # Add tags filter
        if tags:
            cleaned_tags = [tag.strip() for tag in tags if tag.strip()]
            if cleaned_tags:
                conditions.append(BeanieIn(Question.tags, cleaned_tags))

        # Execute query with all conditions
        if len(conditions) == 1:
            results = await Question.find(conditions[0]).limit(limit).to_list()
        else:
            results = await Question.find(And(*conditions)).limit(limit).to_list()

        return results